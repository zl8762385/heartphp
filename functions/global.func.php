}

/**
 * select radio checkbox for explode
 * $target = ,1,2,
 * $source = array();
 */
function box_explode($target, $source) {
	if(empty($target) || empty($source)) return false;

	$target_data = array_filter(explode(',', $target));

	$rt = array();
	if(is_array($target_data) && !empty($target_data)) {
		$source_arr = string2array($source);
		if(isset($source_arr['box'])) {
			$source_data = explode("\n", $source_arr['box']);
			if(!empty($source_data)) {
				$_data = array();
				foreach($source_data as $k => $v) {
					$exp = explode("|", $v);
					$_data[trim($exp[1])] = $exp[0];
				}

				foreach($target_data as $k => $v) {
					$rt[] = $_data[$v];
				}
			}
		}
	}

	return (is_array($rt) && !empty($rt)) ? implode(' ', $rt) : 'dd' ;
}
